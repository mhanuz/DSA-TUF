#include<bits/stdc++.h>
using namespace std;
int main() {
	// numbers and numbers with decimal points
	// int, long, long long takes integer values
	// float and double take integer and decimal points value as well
	int x = 10;
	cout << x << endl;
	long y = 20;
	cout << y << endl;
	long long z = 30;
	cout << z << endl;
	float a = 5;
	float b =10.5;
	cout << a << endl << b << endl;
	double c = 15;
	double d = 20.5;
	cout << c << endl << d << endl;
	// string and getline 
	string s,t;
	cin >> s;
	cout << s;
	getline(cin,t);
	cout << t;
	// char
	char ch = "g";
	cout << ch;
	return 0;
}

